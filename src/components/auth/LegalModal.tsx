export default function LegalModal({ onAgree, onClose }: LegalModalProps) {
  const [checked, setChecked] = useState(false)

  useEffect(() => {
    document.body.style.overflow = 'hidden'
    return () => { document.body.style.overflow = '' }
  }, [])

  function handleAgree() {
    if (!checked) return
    onAgree()
    onClose()
  }

  return (
    <div
      className="fixed inset-0 z-50 flex items-center justify-center p-4"
      onClick={(e) => { if (e.target === e.currentTarget) onClose() }}
    >
      <div className="absolute inset-0 bg-black/50 backdrop-blur-sm" />

      <div
        className="relative bg-card rounded-2xl shadow-2xl w-full max-w-lg flex flex-col overflow-hidden"
        style={{ maxHeight: '85vh' }}
        dir="rtl"
      >
        {/* Header */}
        <div className="flex items-center justify-between px-6 py-4 border-b border-border shrink-0">
          <h2 className="font-black text-foreground text-lg">תנאי שימוש ומדיניות פרטיות</h2>
          <button
            onClick={onClose}
            className="p-1.5 rounded-lg text-muted-foreground hover:bg-muted transition-colors"
            aria-label="סגור"
          >
            <X className="w-4 h-4" />
          </button>
        </div>

        {/* Scrollable content */}
        <div className="flex-1 overflow-y-auto px-6 py-5 space-y-4">
          {SECTIONS.map((s) =>
            s.isTitle ? (
              <h3
                key={s.heading}
                className="text-base font-black text-foreground pt-4 pb-1 border-t border-border first:border-0 first:pt-0"
              >
                {s.heading}
              </h3>
            ) : (
              <div key={s.heading} className="space-y-1">
                <p className="text-sm font-bold text-foreground">{s.heading}</p>
                <p className="text-sm text-muted-foreground leading-relaxed">{s.body}</p>
              </div>
            )
          )}
        </div>

        {/* Sticky footer */}
        <div className="shrink-0 border-t border-border px-6 py-4 bg-card space-y-3">
          <label className="flex items-center gap-2.5 cursor-pointer">
            <input
              type="checkbox"
              checked={checked}
              onChange={e => setChecked(e.target.checked)}
              className="h-4 w-4 shrink-0 accent-primary cursor-pointer"
            />
            <span className="text-sm text-foreground font-medium">
              קראתי ואני מסכים/ה לתנאי השימוש ולמדיניות הפרטיות
            </span>
          </label>
          <div className="flex gap-2">
            <Button
              onClick={handleAgree}
              disabled={!checked}
              className="flex-1 bg-primary hover:bg-primary/90 text-white rounded-xl font-bold disabled:opacity-50"
            >
              אישור
            </Button>
            <Button
              onClick={onClose}
              variant="outline"
              className="flex-1 rounded-xl font-bold"
            >
              סגור
            </Button>
          </div>
        </div>
      </div>
    </div>
  )
}
